"""
Playwright CI/CD Tests
======================
Tests optimized for CI/CD pipelines.
"""

import pytest
from playwright.sync_api import Page, Response
import time
import os


@pytest.fixture
def api_base_url():
    """API base URL from environment or default."""
    return os.getenv("API_BASE_URL", "http://localhost:8000")


@pytest.fixture
def ci_timeout():
    """Timeout for CI environments."""
    return int(os.getenv("CI_TIMEOUT", "30000"))


class TestPlaywrightCIFast:
    """Fast tests for CI."""
    
    @pytest.mark.playwright
    @pytest.mark.ci
    @pytest.mark.fast
    def test_health_check_ci(self, page, api_base_url, ci_timeout):
        """Fast health check for CI."""
        response = page.request.get(f"{api_base_url}/health", timeout=ci_timeout)
        assert response.status == 200
    
    @pytest.mark.playwright
    @pytest.mark.ci
    @pytest.mark.fast
    def test_api_accessible_ci(self, page, api_base_url, ci_timeout):
        """Fast API accessibility check for CI."""
        response = page.request.get(f"{api_base_url}/health", timeout=ci_timeout)
        assert response.status in [200, 503]
    
    @pytest.mark.playwright
    @pytest.mark.ci
    @pytest.mark.fast
    def test_response_time_ci(self, page, api_base_url, ci_timeout):
        """Fast response time check for CI."""
        start = time.time()
        response = page.request.get(f"{api_base_url}/health", timeout=ci_timeout)
        elapsed = time.time() - start
        
        assert response.status == 200
        assert elapsed < 3.0, f"Response too slow for CI: {elapsed:.3f}s"


