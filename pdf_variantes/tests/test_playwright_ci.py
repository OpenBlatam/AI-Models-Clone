class TestPlaywrightCIParallel:
    """Parallel tests for CI."""
    
    @pytest.mark.playwright
    @pytest.mark.ci
    @pytest.mark.parallel
    def test_parallel_health_checks_ci(self, browser, api_base_url, ci_timeout):
        """Parallel health checks for CI."""
        import concurrent.futures
        
        def make_request():
            context = browser.new_context()
            page = context.new_page()
            try:
                return page.request.get(f"{api_base_url}/health", timeout=ci_timeout)
            finally:
                page.close()
                context.close()
        
        with concurrent.futures.ThreadPoolExecutor(max_workers=5) as executor:
            futures = [executor.submit(make_request) for _ in range(10)]
            results = [future.result() for future in concurrent.futures.as_completed(futures)]
        
        assert len(results) == 10
        assert all(r.status == 200 for r in results)


